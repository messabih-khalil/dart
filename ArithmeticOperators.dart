void main() {
  double a = 1;
  double b = 5;

  // addition

  double result = a + b;
  print("$a + $b = $result");

  // substraction

  result = a - b;

  print("$a - $b = $result");

  // multiply
  result = a * b;
  print("$a * $b = $result");

  // division
  result = a / b;

  print("$a / $b = $result");
  
}
